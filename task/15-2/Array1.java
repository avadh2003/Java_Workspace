package task15_2;

public class Array1 
{
	public static void main(String[] args) 
	{
		int number1[] = new int[4];
		number1[0] = 9;
		number1[1] = 5;
		number1[2] = 3;
		number1[3] = 2;
		
		for(int j = 0; j <= number1.length; j++)
		{
			System.out.println(number1[j]);
		}
	}

}
